async function run() {
  try {
    // First delete existing single questions (keep structure, replace questions)
    for (const [oldId, info] of Object.entries(assessmentMap)) {
      const [[row]] = await pool.query(
        'SELECT a.id FROM assessments a JOIN sections s ON a.section_id=s.id WHERE a.title=? AND a.type=?',
        [info.title, info.type]
      );
      if (!row) { console.log(`SKIP: ${info.title} not found`); continue; }

      const assessId = row.id;
      // Delete old placeholder questions
      await pool.query('DELETE FROM questions WHERE assessment_id=?', [assessId]);
      console.log(`Cleared questions for: ${info.title}`);

      // Insert all questions
      const qs = questions[parseInt(oldId)];
      for (let i = 0; i < qs.length; i++) {
        const q = qs[i];
        await pool.query(
          'INSERT INTO questions (id, assessment_id, question_text, options, correct_answer, order_index) VALUES (?,?,?,?,?,?)',
          [uuidv4(), assessId, q.q, JSON.stringify([q.a, q.b, q.c, q.d]), q.correct, i+1]
        );
      }
      console.log(`✅ ${info.title}: ${qs.length} questions added`);
    }

    // Verify
    const [counts] = await pool.query(`
      SELECT a.title, a.type, COUNT(q.id) AS q_count
      FROM assessments a LEFT JOIN questions q ON q.assessment_id=a.id
      GROUP BY a.id ORDER BY a.title, a.type
    `);
    console.log('\n=== FINAL QUESTION COUNTS ===');
    counts.forEach(r => console.log(`${r.title} [${r.type}]: ${r.q_count} questions`));

    const [[{total}]] = await pool.query('SELECT COUNT(*) as total FROM questions');
    console.log(`\nTotal questions in DB: ${total}`);
  } catch(e) {
    console.error(e.message);
  } finally {
    process.exit(0);
  }
}
run();
